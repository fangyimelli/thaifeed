export function assertChatPoolsCounts(): { total: number; poolCounts: Record<keyof ChatPools, number> } {
  const poolCounts = {
    casual_pool: CHAT_POOLS.casual_pool.length,
    observation_pool: CHAT_POOLS.observation_pool.length,
    theory_pool: CHAT_POOLS.theory_pool.length,
    thai_viewer_pool: CHAT_POOLS.thai_viewer_pool.length,
    fear_pool: CHAT_POOLS.fear_pool.length,
    guess_character: CHAT_POOLS.guess_character.length,
    tag_player: CHAT_POOLS.tag_player.length,
    san_idle: CHAT_POOLS.san_idle.length,
    vip_summary: CHAT_POOLS.vip_summary.length,
    final_fear: CHAT_POOLS.final_fear.length
  } satisfies Record<keyof ChatPools, number>;

  for (const key of Object.keys(EXPECTED_POOL_COUNTS) as Array<keyof ChatPools>) {
    const expected = EXPECTED_POOL_COUNTS[key];
    const actual = poolCounts[key];
    if (actual !== expected) {
      throw new Error(`[chat_pools] ${key} expected ${expected}, got ${actual}`);
    }
  }

  const total = Object.values(poolCounts).reduce((sum, value) => sum + value, 0);
  if (total !== 2050) {
    throw new Error(`[chat_pools] total expected 2050, got ${total}`);
  }

  return { total, poolCounts };
}
